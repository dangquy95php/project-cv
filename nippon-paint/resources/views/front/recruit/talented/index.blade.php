    <h1 class="xxxxx-large">求める人材像</h1>
    </div><!-- TITLE-CLM -->

    <div id="LEFT-CLM">
    <div class="clm">

    <p>日本ペイントは汎用塗料および自動車補修用塗料マーケットをターゲットに事業を展開しています。<br>
    このマーケットにおいて、創業以来長い歴史の中で培ってきた製品力と全国の販売ネットワークは、当社にとっての強みであり、今後当社が更に発展していくためのかけがえのない資産です。</p>

    <div class="ph-clm dr-K">
        <p><img src="/images/recruit/talented_img_02.jpg" width="300" height="180"></p>
        <p><img src="/images/recruit/talented_img_01.jpg" width="300" height="180"></p>
    </div><!--.clm-->

    <p>この強みは、お客さまとのコミュニケーションのなかで当社を「信頼」して頂いた結果に他なりませんが、そこには、次のような人材像が浮かび上がってきます。</p>

    <p><span class="txt-01">モノづくりマインド（モノづくりの社会に果たす役割についての強い関心/好奇心/誇り）</span>をベースに、<br>
    <span class="txt-02">「自ら考え行動できる人」</span>（お客さまの課題解決力）<br>
    <span class="txt-02">「チームワークを大切にでき達成意欲が高い人」</span>（目標達成に向け周囲を動かす力）<br>
    <span class="txt-02">「誠実で明るく元気な人」</span>（心技体ともに充実した状態を維持する自己管理力）
    </p>

        <p>当社ではこのような人材像を思い浮かべながら、学生のみなさんとの対話を通じて選考をさせて頂いています。<br>
    選考の過程では、これら人材像のみに囚われることなく、みなさんの“個性”にもスポットを当て、多様な人材がお互いに認め合い、協力して働ける会社づくりのための採用を行っています。</p>
        <p>「塗料」を通じて、多くのお客さまに、より満足のいく提案を届けたい。そんな想いのもとに、会社は絶えず進化を続けています。<br>
    日本ペイントの未来を担う、みなさんのチャレンジをお待ちしています。</p>

