    <div id="TITLE-CLM">
    <h1 class="xxxxx-large">日本ペイントの人材育成について</h1>
    </div><!-- TITLE-CLM -->

    <div id="LEFT-CLM">
    <div class="clm">

    <p>会社が持続的に成長、発展していくための基盤は「人」に他なりません。<br>
    社員一人ひとりがいきいきと「働きがい」を感じながら能力を最大限に発揮してもらうために、<br>
    日本ペイントでは人材育成を経営と社員にとっての最重要テーマのひとつと捉えています。</p>

    <p>人材育成は社員本人の成長意欲と自助努力を基本とし、経営はスポンサーシップを通じて人材育成に責任を持ちます。<br>
    そのため、人材育成について以下の方針に沿った教育体制を整えています。</p>
    </div><!--.clm-->


    <div class="clm">
        <h2>人材育成方針</h2>
        <ul class="list">
            <li>教育、育成の基本は職場での育成OJT</li>
            <li>OJTを補完する目的で、Off-JT（研修）を実施する</li>
            <li>研修は機会均等型研修と階層別研修/職種別研修など育成目的を明確化する</li>
        </ul>
    </div><!--.clm-->

    <div class="clm">
        <h2>OJTについて</h2>
        <p>OJTでは指導員制度を導入しており、新入社員を指導員の担当先輩社員が１年間つきっきりで指導するシステムとなっています。<br>
    目標を立てたうえで、先輩からアドバイスを受けながら、日本ペイントの社員として成長していくための仕組みです。<br>
    また２年目以降も、職場では先輩社員や上司が必要に応じてさまざまな指導をしていくOJTの風土が確立されています。
    </p>
    </div><!--.clm-->

    <div class="clm">
        <h2>Off-JT（研修）について</h2>
        <p>Off-JTとして次のような研修を用意しています。</p>

        <div class="clm">
            <h3 class="ttl-01">新入社員研修</h3>
            <p>日本ペイントホールティングスグループ全体でおこなう集合研修。ビジネスマナーや製品、業界知識の習得などを目的に充実したプログラムを準備しています。</p>
        </div>
        <div class="clm">
            <h3 class="ttl-01">調色工場・流通センター実習</h3>
            <p>実際の生産現場に赴き、製造業務や受発注業務などの実習をおこない、顧客と製品、社内の仕組みについて知識を習得し「営業基礎力」を養います。同時に社内の人脈を構築し、配属後にスムーズにテイクオフしてもらうことも目的の一つです。</p>
        </div>

        <p>上記のような機会均等型の研修のほか、階層別/職種別の研修もラインナップしています。<br>これらを通じ、仕事の幅や裁量を広げキャリアップにつなげて欲しいと考えています。
    </p>

    </div><!--.clm-->

