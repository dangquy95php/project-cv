        <h2 class="ttl-07">製・販・装が一体となり、さらに価値をプラスする。</h2>
        <p class="clm-06">塗料の価値は、最終消費者（建築工事の発注者や自動車のオーナーなど）に対し、「製（造）・販（売）・（塗）装」の流れで届けられるという点が特徴です。塗料がお客さまに届けられるまでには、当社が製造した製品を全国の施工店に届ける販売店や、最終消費者の建物や自動車を彩り、仕上げる施工店の力が不可欠です。<br>
