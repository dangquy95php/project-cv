    <section class="p-support-infor-fire3">
        <div class="l-cont2">
            <x-title2>
                <x-slot name="title2_class">c-title2--small</x-slot>
                <x-slot name="title2_text">どんな時に、塗料が自然発火するのか？</x-slot>
            </x-title2>

            <p class="c-text1">塗料やワックスなどの油類は、酸化反応することで発熱します。その主な条件は「酸素」「温度」「密度」の３つ。油類は酸素に触れることで酸化し、酸化熱を発生。その熱エネルギーは、温度が高い状態でさらに増幅します。そして、密度が高ければ高いほど熱がこもり、発火につながるのです。</p>
            <div class="u-center">
                <img src="/images/support/information/fire_img2.svg" alt="どんな時に、塗料が自然発火するのか？">
            </div>
            <p class="c-text1">たとえば、<strong>塗料やワックスを拭き取った布、油が染み込んだ紙、養生に使ったシート</strong>などを、<strong>山積み</strong>したり、容器にまとめて入れたり、<strong>ビニール袋に入れたまま放置</strong>するといった原因が考えられます。</p>

            <div class="c-box2">
                <p class="c-box2__text1">塗膜は自然発火しません</p>
                <p class="c-box2__text2">塗膜（塗料が固まって膜となったもの）は、薄く塗り広げられた状態のため、熱がこもることはなく、自然発火は起こりません。一般的な塗膜の厚みでは、酸化反応による発熱より熱放射の方が早く、熱エネルギーが蓄積されることがないからです。</p>
            </div>
        </div>
    </section><!-- / p-support-infor-fire3 -->

