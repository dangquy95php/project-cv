            <h2 class="c-title2 c-title2--small">タフガードQ-R 工法の概要</h2>
            <h3 class="c-title2 c-title2__style3">NETIS（国土交通省 新技術情報提供システム） 登録No. KK-040054-VE （掲載終了）</h3>
            <ul class="c-list18">
                <li class="c-list18__item">
                    <h3 class="c-title2 c-title2__style5"><span>1. </span>工期・工程短縮</h3>
                    <p class="c-text1">乾燥時間わずか2時間（23℃）の超速乾システム。低温時の硬化性も抜群です。さらに、ガラスクロスなど補強層工程が不要なため、省工程になり工期を大幅に短縮します。</p>
                </li>
                <li class="c-list18__item">
                    <h3 class="c-title2 c-title2__style5"><span>2. </span>超防食性能</h3>
                    <p class="c-text1">抜群の高弾性（伸張率45％）により素地クラックによく追従します。また衝撃性・耐摩耗性にも優れ、塩害、凍害、中性化、アルカリ骨材反応などの防止に抜群の性能を発揮します。</p>
                </li>
                <li class="c-list18__item">
                    <h3 class="c-title2 c-title2__style5"><span>3. </span>優れた施工性</h3>
                    <p class="c-text1">省工程・超速乾システムにより高所作業車での施工効率を大幅に向上します。こてによる施工で、狭隘部の作業性にも優れます。</p>
                </li>
                <li class="c-list18__item">
                    <h3 class="c-title2 c-title2__style5"><span>4. </span>コストダウン</h3>
                    <p class="c-text1">補強層工程カットによる省工程と超速硬化により作業工数の低減を図ることが出来ます。これにより、共通仮設費の低減などコストパフォーマンスが期待できます。</p>
                </li>
                <li class="c-list18__item">
                    <h3 class="c-title2 c-title2__style5"><span>5. </span>環境への配慮</h3>
                    <p class="c-text1">タフガードQ-R 工法は環境に優しい無溶剤塗料を使用しています。こてによる施工のためダスト飛散がありません。</p>
                </li>
                <li class="c-list18__item">
                    <h3 class="c-title2 c-title2__style5"><span>6. </span>責任施工</h3>
