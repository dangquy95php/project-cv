            <p>地球に降り注ぐ紫外線の量は、増加傾向にあると言われています。紫外線が増加すると、どうなるのでしょうか。数々の研究から、紫外線を浴びすぎると人体にさまざまな悪影響を及ぼすことがわかってきました。</p>
            <p>シワやシミの発生からお肌をまもるために、紫外線対策をされている方も少なくないでしょう。しかし紫外線対策が必要なのは、お肌だけではありません。紫外線は、皆さまの大切なお住まいにも悪影響を及ぼしているのです。人々の快適な暮らしをまもっている建物の外壁に塗られている塗膜は、紫外線が大の苦手なのです。紫外線を浴び続けることで塗膜は破壊され、美観を損ねるだけでなく、放っておけば建物の劣化の原因にもなります。紫外線はお肌の敵であると同時に、お住まいの大敵でもあるのです。</p>
