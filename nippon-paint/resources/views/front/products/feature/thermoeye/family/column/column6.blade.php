                                <h4>コップのお湯はなぜすぐ冷める？</h4>
                                <p>お風呂よりコップのお湯の方が早く冷めるのは、表面積と体積の関係にあります。例えば、１m×１m×２mのお風呂と10cm×10cm×20cmのコップの場合、中身は同じ温度でもトータルの熱エネルギーが体積に比例するため、1000倍の違いがあります。しかし、熱の放出は表面積に比例するため、冷めやすさは 100倍しか違いません。さらに浴槽は断熱構造で、側面や底面にはほとんど熱が放出されないため、冷めにくいのです。</p>
