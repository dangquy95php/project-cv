                    <div id="q4" class="faq-question">
                        <p class="q-title pngimg">Question.04</p>
                        <h3>遮熱したら、夏はクーラーいらないの？</h3>
                        <div class="faq-answer pngimg">
                            <p>夏、部屋の温度が上がるのは、外から部屋に熱が伝わって入ってくるからです。<br>中に入ってくる熱の原因は主に、太陽から直接受け取る熱と、外気温から伝わってくる熱があります。<br>遮熱塗料は、太陽光が熱にかわるのを抑える機能を持つので、前者の影響を小さくすることができます。<br>しかしＱ２のように、遮熱塗料には保温・断熱効果がないため、外気温から伝わってくる熱を抑える効果はありません。<br>そのため、クーラーがいらなくなるわけではありませんが、クーラーの負担を小さくすることができます。</p>
                        </div>
                    </div>

                    <div id="q5" class="faq-question">
                        <p class="q-title pngimg">Question.05</p>
                        <h3>家の中で、遮熱の効果を感じることはできますか？</h3>
                        <div class="faq-answer pngimg">
                            <p>遮熱した効果は、塗装した屋根の直下の部屋で感じ取ることができます。<br>しかし、屋根の表面温度が最大で１０℃下がったとした場合でも、その部屋で何℃下がるのか？どのくらい効果を感じるか？は、その家が持つ断熱性能によってかなり違いが出ます。<br>断熱性能が高い家では、断熱性能が低い家とくらべて、もともと外から部屋に入ってくる熱がとても少ない状態（外部の熱環境の影響を受けにくい状態）にあるため、室内では遮熱した効果が出にくくなります。<br>家の断熱性能は、壁の中に入っている断熱材の厚さ（何センチか？）や気密性、木造か鉄骨造かコンクリート造かなど、その家の構造によって違います。<br>体感効果としては、天井の表面温度が下がることで、実際の室温低下以上に感じられる場合もあります（体感温度の感じ方には個人差があります）。</p>
