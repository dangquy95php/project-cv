                    <div id="q1" class="faq-question">
                        <p class="q-title pngimg">Question.01</p>
                        <h3>遮熱塗料の効果はいつも一定ですか？</h3>
                        <div class="faq-answer pngimg">
                            <p>遮熱塗料で塗り替えても温度は常に一定に下がるわけではありません。それは、太陽の角度や風の強さ、屋根の素材や色、換気やエアコンなど、様々な要素が室内の温度に影響を与えているからです。<br>効果を最大限に発揮するためには、日射反射率が高い色相を選ぶようにしましょう。同時に、地域との調和に配慮するようにしましょう。</p>
                        </div>
                    </div>

                    <div id="q2" class="faq-question">
                        <p class="q-title pngimg">Question.02</p>
                        <h3>太陽熱高反射塗料と断熱材ってどこがちがうの？</h3>
                        <div class="faq-answer pngimg">
                            <p>同じような効果があるように見えますが、実は意味あいが違います。太陽熱高反射は、太陽の光エネルギーを鏡のように反射することで、熱を発生しにくくする技術のこと。いっぽう断熱は、断熱材を布団のように分厚くして、熱を伝わりにくくする働きをもちます。ですから太陽熱高反射塗料に保温効果はありません。断熱材には、表面温度の上昇を抑制する効果（ヒートアイランド対策）はありません。</p>
                        </div>
                    </div>

                    <div id="q3" class="faq-question">
                        <p class="q-title pngimg">Question.03</p>
                        <h3>遮熱すると、冬場は屋根の温度が下がって損をする？</h3>
                        <div class="faq-answer pngimg">
                            <p>夏の日射エネルギーを最大1000W/㎡とすると、例えば日射反射率50%の塗料の場合、500W/㎡の熱エネルギーを受け取ります。しかし、冬の日射は最大でも400W/㎡程度なので、受け取る熱エネルギーは200W/㎡。年間で考えると夏に500W/㎡分お得で、冬に200W/㎡分損をしていることになるため、差し引き300W/㎡分おトクです。<br>
    （分かりやすさのために単純化しています）<br>
    このとき大切なことは、損得とは別に、環境貢献できることです。年間を通して日射エネルギーの50％分を熱にかえないことで、ヒートアイランド対策や地球温暖化対策に貢献することができます。屋根ひとつだけでできる貢献は小さいかもしれません。でも、もしも町全体・日本全体の屋根で日射反射率が向上したら、その効果は決して小さなものではありません。せっかく屋根を好きな色でキレイに塗り替えるなら、日射反射率の高い遮熱塗料で塗り替えて、ココロも気持ちよくキレイになれたらいいですね！<br><br>
    さらに近年では、環境貢献ということに加え、社会貢献という考え方もクローズアップされています。<br>
    夏の猛暑日、昼間のピーク時には非常に大きな電力需要が生まれます。多くの人々が同時に冷房を使用することは、その大きな要因のひとつです。電力会社ではその電力需要を支えるために、ピーク時間帯にも対応できる電力供給能力を確保しなければなりません。現在はまだ発電した電力を大量に貯蔵しておくことができないため、電気は必要な量を必要なときに確保しなればならないのです。もしも電力需要が電力供給能力を超えてしまったら、電気の供給そのものがストップしてしまう可能性もあると言われています。<br>
    そのため環境貢献の視点でトータルの電力使用量を減らすことも大切ですが、社会貢献の視点からピーク時の使用電力を減らすことも求められています。遮熱することで少しでも夏場のピーク電力を減らすことは、小さな社会貢献のひとつなのです。<br>
    （冬期にも暖房による電力需要のピークが発生しますが、多くの場合それは気温が低下する夜に発生します。夜には日射がありませんので、遮熱が冬期のピーク時電力需要を増やしてしまうことは非常に少ないと考えられます。）</p>
                        </div>
