                <div id="mecha1_clm1" class="chapter-block clearfix">
                    <h2><span>■</span>建物の遮熱とは？</h2>
                    <div class="img-block-r">
                        <img src="/assets/products/feature/thermoeye/factory/mechanism/images/tefc_mecha01_img01.gif" width="390" height="251" alt="イメージ" class="img-right">
                    </div>
                    <p>ここで言う遮熱とは、太陽の光を反射させて屋根の蓄熱を抑える働きのこと。太陽の光エネルギーは、屋根に当たって熱エネルギーへと変化し、屋根そのものの温度を上昇させます。屋根から熱が伝わると、室内の温度も次第に上がっていきます。遮熱塗料はこのメカニズムに注目したもので、太陽光を多く反射することで屋根の温度上昇を最小限に抑制します。（右図1参照）<br><br>このような働きから、遮熱塗料にはさまざまな効果が期待できます。まず、屋根の蓄熱を抑えることで、地球温暖化やヒートアイランド対策にも貢献。さらに、室内温度を下げることで省エネやコストダウンを実現し、同時にCO<sub>2</sub>排出量の削減にもつながります。</p>
                </div>

                <div id="mecha1_clm2" class="chapter-block clearfix">
                    <h2><span>■</span>室内温度の決定条件</h2>
                    <div class="img-block-r">
                        <img src="/assets/products/feature/thermoeye/factory/mechanism/images/tefc_mecha01_img02.jpg" width="459" height="310" alt="イメージ" class="img-right">
                    </div>
                    <p>室内の温度を上昇させるのは、主に太陽熱や外気温の働き。しかし、太陽の角度や風の強さ、屋根の素材・色によってその温度上昇率は変わってきます。<br><br>また、機械や電気製品の稼働率や人・モノの多さ、換気やエアコンなども温度を左右する大きな要因に。このように複雑な条件が絡み合い、室内の温度が決定されるのです。</p>
                </div>

                <div id="mecha1_clm3" class="chapter-block clearfix">
                    <h2><span>■</span>遮熱塗料の働き</h2>
                    <div class="img-block-r">
                        <img src="/assets/products/feature/thermoeye/factory/mechanism/images/tefc_mecha01_img03.gif" width="390" height="239" alt="イメージ" class="img-right">
                    </div>
                    <p>太陽の光エネルギーは、約50%が赤外線、約47%が可視光、残りの3%は紫外線から成り立っています。遮熱塗料が果たす役割は、太陽光による赤外線を効率よく反射させること。右のグラフは、ほぼ同じ色の従来塗料と遮熱塗料の日射反射率を領域ごとに比較したものです。<br><br>日射反射率とは、太陽の光エネルギーをどれぐらい反射するかを表す指標のこと。この数値が大きいほど、遮熱性能が高いことを意味しています。比較図（上の図1）からもわかるとおり、遮熱塗料はより多くの赤外線を反射するため、その分の熱エネルギーの吸収を抑えることができ、室内の温度上昇を抑える効果が期待できると実証されています。</p>
                </div>

                <div id="mecha1_clm4" class="chapter-block clearfix">
                    <h2><span>■</span>太陽熱高反射塗料と断熱材の違いは？</h2>
                    <div class="img-block-r">
                        <img src="/assets/products/feature/thermoeye/factory/mechanism/images/tefc_mecha01_img04.gif" width="310" height="278" alt="イメージ">
                        <p class="img-caption">※1／表面に取り残される熱量が増えるため断熱材があるほうが、室外表面の温度は上がりやすい</p>
                    </div>
                    <p>同じような効果があるように見えますが、意味合いは別のものです。この違いは、熱エネルギーへの考え方でわかります。太陽の光エネルギーは、屋根に当たると熱エネルギーへ変化し、屋根の温度を上昇させます。この熱が家の中まで伝わり、室内の温度が高くなっていきます。<br>断熱材とは、熱エネルギーが通りにくい素材のこと。この素材を厚くすればするほど、室内に流れる熱エネルギーを小さくできます。つまり、屋根からの熱を室内に伝わりにくくするのが、断熱の特徴です。一方、太陽の光エネルギーを反射して、屋根の温度上昇を妨げるのが太陽熱高反射塗料です。太陽熱高反射塗料は、太陽光が熱エネルギーに変わる前に、塗膜部分でその大半を反射。屋根が温まるのを未然に防ぎ、室内の温度上昇も防ぐことができるのです。さらに、太陽の光が熱へ変化することを抑える効果があるので、地球温暖化やヒートアイランド対策に貢献できるのです。</p>
                </div>
