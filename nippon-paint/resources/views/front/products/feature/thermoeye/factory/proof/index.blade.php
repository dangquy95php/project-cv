                <div id="prf2" class="chapter-block clearfix">
                    <h2><span>■</span>共同学術研究レポート</h2>
                    <h3>研究の背景と目的</h3>
                    <p>近年、都市のヒートアイランド化が深刻な問題となっており、様々なヒートアイランド技術対策が行われている。そんな中でヒートアイランドを抑制する技術として遮熱塗料が注目されている。地表面や建物表面の日射熱吸収を抑制し、屋内外の温熱環境改善効果が期待できる遮熱塗料はどの程度の効果があるのか？調査を行うことで、遮熱塗料の優位性を検証する。</p>
                    <h3>研究の概要</h3>
                    <p>遮熱塗料の屋内外温熱環境および空調電力消費量に対する影響を検証するため、愛知工場内の事務所棟における実測調査を行った。また、同敷地内に2基の比較実験棟を用意し、遮熱塗料と一般塗料の比較実験を実施。夏期、中間期、冬期で計測を行い、屋内外温度と電力消費量を検証する。</p>
                    <h3>結果まとめ</h3>
                    </h3></h3></h3><h4>＜事務所棟実測調査＞</h4>
                    <p>塗装前後の反射率を計測調査<br>塗装前　<span class="redtext">約13％</span>　→塗装後　<span class="redtext">約52％</span>に<br>塗装後の方が約<span class="redtext">39ポイント</span>高い結果に</p>
                    <h5>夏期の実測結果</h5>
                    <p>塗装前と比較して表面温度が<span class="redtext">8〜10℃低下</span><br>月ごとの空調電力消費量　塗装後の方が8月で<span class="redtext">12.6％</span>、9月で<span class="redtext">25.9％の削減</span>と1割以上の削減効果が得られている</p>
                    <h5>中間期の実測結果</h5>
                    <p>塗装前と比較して表面温度が<span class="redtext">8〜10℃低下</span></p>
                    <h5>冬期の実測結果</h5>
                    <p>塗装前後の表面温度に大きな差は無かった。<br>月ごとの空調電力消費量では、塗装後の方が12月で<span class="redtext">6.0％の増加</span>、1月はほぼ増減無し、気温の高めだった2月では<span class="redtext">20.7％</span>の削減となった。<br><br>年間を通じた結果としては、冬期の暖房負荷の増加がほとんど見られず、夏期の空調負荷削減効果がほぼそのまま現れる形となった。</p>
                    <h4>＜比較棟実測調査＞</h4>
                    <h5>夏期の実測結果</h5>
                    <p>一般塗料と比較して、天井表面温度で<span class="redtext">約4〜5℃</span>、屋内の南可部表面温度で<span class="redtext">3〜5℃</span>、室内温度で<span class="redtext">約2〜3℃</span>低くなった。
    空調電力消費量も<span class="redtext">5.9％</span>程度、さらに空調時に1.5℃程度遮熱実験棟の方が低く推移していた室温差を補正した場合で<span class="redtext">8.2％</span>の削減効果が見られた</p>
                    <h5>冬期の実測結果</h5>
                    <p>一般塗料の場合と、遮熱塗料の場合で、室内温度に大きな差はなかった。
    夏期に比べて日射量が少ないため外装の反射率の影響が小さいことや、両棟の気密性の違いなど考えなければならない問題はあるが、本実測においては、遮熱塗料による暖房負荷の影響は非常に小さいことが確認されたと言える。<br><br>以上の結果から建物規模、断熱仕様、使用用途によって、遮熱塗料の有用性は大きく変化するが、本実測においては、暖房負荷よりも冷房負荷の方が大きく、通年の電力消費削減、屋内外温熱環境改善効果に大きく寄与できると言える。</p>
