                <h1>遮熱というスタンス</h1>
                <p>社会のエコ意識が年々高まる中、環境保全への取り組みは今や特別なことではなく、もはや「当たり前」のこととなりました。企業の社会的責任にも高い注目が集まり、業界や業種を問わず、ほとんどの企業が何らかのかたちで環境保全活動に取り組んでいます。<br>
                「同じ取引をするなら、地球にやさしい企業がいい」「エコに積極的な企業は、時代に合ったビジネスができる」<br>社会では、このような意識も根付きはじめています。そう、エコが企業価値の向上につながる、そんな時代が到来したのです。<br><br>
                特に、環境負荷への固定イメージが強い工場においては、これまで以上に積極的な環境貢献が求められているのではないでしょうか？<br>
                工場からは、毎日おびただしい数値の熱量が排出されています。その排熱はヒートアイランド現象を引き起こし、やがては環境破壊の一因となることも否めません。<br>
                <br>
                そのような現実を受け、私たち日本ペイントは、工場からの排熱を極力抑える遮熱塗料「サーモアイシリーズ」を開発しました。排熱量を減らせば、地球環境対策に貢献できるだけでなく、空調コストの削減も実現可能に。<br>“遮熱というスタンス”。私たち日本ペイントが、工場をエコ先進工場へと塗り替えます。</p>

                <div id="concept-clm1" class="clearfix">
                    <h2>ヒートアイランド現象と温暖化。　Warming temperature</h2>
                    <h3><span>■</span>すぐそばにある環境問題。 深刻化する、その影響と原因。</h3>
                    <p>今世紀最大の社会問題と言われる地球温暖化。主に石油などの化石燃料の大量消費によって発生したＣＯ<sub>2</sub>は、熱を吸収する性質が強い「温室効果ガス」と呼ばれています。<br><br>この温室効果ガスが年々増加する一方で、森林破壊などによりＣＯ<sub>2</sub>の吸収が追いつかず、地球全体の気温が上昇し続けていると言われています。これにより、平均海面水位の上昇や異常気象、自然生態系、さらには人間の健康への影響が心配されています。<br><br>そして、私たちが直面するもうひとつの環境問題が、都心部のヒートアイランド現象。ヒートアイランド現象とは、都心部の気温が郊外よりも高くなる現象のこと。エアコンや自動車、工場などから排出される人工排熱や、コンクリートやアスファルトなどによる地表面の人工化によって、蓄熱されることが大きな原因とされています。都市内外の気温差は年間を通じてありますが、特に風のない夜間はこの差が大きく、また夏よりも冬の方が差が大きくなる傾向にあります。その気温差は、ときには５℃以上になることも。これらの地球環境問題に向けて、国内外ではさまざまな取り組みが進められているのです。</p>
