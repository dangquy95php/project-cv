                <div id="concept-clm2" class="clearfix">
                    <h2>高まる環境活動とCO2削減。　Environmental practice</h2>
                    <div class="clearfix">
                        <div class="text-block">
                            <h3><span>■</span>世界の取り組み</h3>
                            <p>世界の共通問題として環境問題への取り組みがかたちとなったのが、記憶にも新しい京都議定書の採択。日本では−6%、アメリカは−7%、EU諸国は−8％（1990年比）というように、各国単位で温室効果ガス排出量の削減数値目標を設定し、同時に、国を問わず地球規模で削減目標を達成するための仕組み（京都メカニズム）が導入されました。<br>また、各国でカーボンオフセットやグリーン購入などの取り組みが積極的に行われ、エコ先進諸国であるEU諸国では環境法や環境税の導入をいち早く実施。また、国によってばらつきはあるものの、環境対策全般の専門家である環境カウンセラーや環境コンサルタントを制度化する国々も登場し始めました。このように、世界中でさまざまな環境活動が実施されており、今後もますます高いレベルでの取り組みが求められています。</p>
                        </div>
                        <div class="text-block">
                            <h3><span>■</span>日本国内の取り組み</h3>
                            <p>日本においても、環境問題に関する意識はかつてない高まりを見せています。政府や自治体では、循環型社会の構築に向けた取り組みやグリーン購入法、チームマイナス６％の推進などが実施され、生活者の間でも、マイバッグやマイ箸の持参、ゴミの分別、省エネ商品への買い換えなど暮らしに密着したエコ活動が幅広く浸透しています。<br>また、業種を問わずさまざまな企業で植樹活動やゴミゼロ運動、リサイクルシステムの導入、エコ型商品やサービスの開発など、積極的な取り組みが行われています。こうした背景の中、企業にとっては「いかに環境問題に貢献しているか」が問われる時代。経済活動によって生み出された環境破壊は、経済活動と環境活動の両輪で修復されることが求められているのです。</p>
                        </div>
                    </div>
                    <div class="clearfix">
                        <div class="text-block">
                            <h3><span>■</span>企業価値とエコの関係</h3>
                            <p>エコとビジネスの両立は、企業にとって非常に大きなテーマのひとつ。産業社会の一員であるかぎり、企業の社会的責任という意味でも環境問題への貢献は欠かすことができません。なかでも、大量のCO<sub>2</sub>や排熱などを生み出す工場にとって、環境への配慮はイメージおよび価値向上の大きな一助となるはず。<br><br>このような観点からも、今、日本中のさまざまな工場でCO<sub>2</sub>削減への取り組みが行われています。そんな中、大きな注目を集めているのが、遮熱塗料を使った環境貢献。工場建物や敷地内のアスファルトに塗装することで、ヒートアイランド対策はもちろん、空調設備によるCO<sub>2</sub>排出量を減らすことができるのです。空調による消費電力量が削減できれば、電力コストもカット。現場で働くスタッフも、美しく快適な職場で業務に取り組むことができます。</p>
