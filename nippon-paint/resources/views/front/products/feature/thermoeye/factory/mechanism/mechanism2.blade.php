                    <h2><span>■</span>遮熱塗料の効果で、屋根や室内の温度はいつも一定？</h2>
                    <p>遮熱塗料で屋根を塗り替えても、屋根や室内の温度は常に一定の下がり方をするわけではありません。室内の温度を上昇させるのは、主に屋根から伝わる太陽熱や外気温の働き。工場や倉庫には断熱材のない構造の建物が多く、この場合、遮熱塗料が室内環境の改善に非常に有効だと言えます。しかし、太陽の角度や風の強さ、屋根の素材・色、換気やエアコンによって温度上昇率は変化します。機械や電気製品などがあれば、その使用エネルギー分が熱エネルギーとなって室内に放出されます。つまり、機械や電気製品を稼働させるほど、室内の温度は高くなっていくのです。<br><br>また、同じ建物でも室内にモノがたくさんある場合と何もない場合も、室内温度に差が生まれます。これは、モノ（物体）が熱エネルギーを溜め込むという性質によるもの。室内が空っぽの場合は、室内に入った熱エネルギーがすぐに室温に反映するため、夏の昼間のピーク室温が非常に高くなります。逆に、室内にモノが多い場合は、これらのモノが熱エネルギーを溜め込み、遅れて少しずつ冷めていくため、ピーク室温は下がり、その下がり方がゆっくりになる傾向にあります。<br>このような複雑な条件により、室内温度は左右されているのです。</p>
