                    <p>まず、「色」とは物体から届く光が人間の目に飛び込んできた際に、その可視光を知覚することで初めて生じる概念です。鏡の場合、当たった可視光をほとんど正反射（鏡面反射）してしまうため、物体から届く光（色）をそのまま反映します。従って物体から届く光によって見える色が変わるため、鏡に定まった色はないといえます。</p>
