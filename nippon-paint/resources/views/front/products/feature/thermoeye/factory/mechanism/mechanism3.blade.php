                <div id="mecha3_clm4" class="chapter-block clearfix">
                    <h2><span>■</span>鏡の色って何色？</h2>
                    <div class="img-block-r">
                        <img src="/assets/products/feature/thermoeye/factory/mechanism/images/tefc_mecha03_img04.gif" width="350" height="215" alt="イメージ">
