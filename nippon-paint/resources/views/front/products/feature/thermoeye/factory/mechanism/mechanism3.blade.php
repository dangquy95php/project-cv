                <div id="mecha3_clm2" class="chapter-block clearfix">
                    <h2><span>■</span>20℃の鉄と20℃の木。 冷たいのはどっち？</h2>
                    <div class="img-block-r">
                        <img src="/assets/products/feature/thermoeye/factory/mechanism/images/tefc_mecha03_img02.gif" width="350" height="205" alt="イメージ">
                    </div>
                    <p>不思議なことに、多くの人が同じ温度でも鉄の方が冷たいと感じます。これは、素材の温度伝導率（温まりやすさ・冷めやすさの差）の違いによって起こるもの。鉄と木では、温度伝導率に約120倍もの差があるため、鉄の方が約120倍温まりやすくなります。そのため、鉄に肌が触れた瞬間、鉄にあっという間に熱が奪われ、冷たいと感じるのです。一方、木は鉄よりも温まりにくいため、鉄よりも温かく感じるのです。</p>
                </div>

                <div id="mecha3_clm3" class="chapter-block clearfix">
                    <h2><span>■</span>地域によって色の見え方は違う？</h2>
                    <div class="img-block-r">
                        <img src="/assets/products/feature/thermoeye/factory/mechanism/images/tefc_mecha03_img03.gif" width="350" height="215" alt="イメージ">
                    </div>
                    <p>地球では、北極や南極に近づくほど太陽光が通る大気層の距離が長くなります。このため、極に近い地域では波長の短い青い光が散乱され、自然光は青みが強調されます。逆に、赤道に近づくほど光の散乱が少なく、極に近い地域と比べて波長の長い赤い光が強調されるのです。たとえば同じ日本でも、北日本では寒色系が、南日本では暖色系の色がキレイに見え、太平洋側と日本海側では主に湿度や日照の影響で、クリアな色とグレイッシュな色の差があると言われています。</p>
