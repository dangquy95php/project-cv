                <h2>"人間が『見たことのない色』がある?"</h2>

                <p class="honmon">　人間の目にある3つのセンサーはそれぞれ下のグラフ（［人間の色のとらえ方］・［鳥の色のとらえ方］）のように、波長によって捉えやすさに違いがあります。人間の眼にあるこの3つのセンサーが、それぞれの波長の光を捉えることで、人間は景色を見ています。グラフの山が高い方が、その波長の光を捉えやすいことを表します。このグラフ見ると、波長550nm付近の波長をとても捉えやすいことが何となく推測できそうです。この550nmという波長は人間がもっとも明るさを感じ取ると言われており、これを特に"明所最大視感度"といいます。<br>
                　それでは、他の動物も同じなのでしょうか？<br>
                実は動物によって、光の捉え方に違いがあることが分かっています。これは、その動物の目にある錐体を調べることで分かります。<br>
                　その中でも、鳥は人間には見えない紫外線が見えると言われています。<br>
                下のグラフ［鳥の色のとらえ方］が、鳥の錐体がどのように光を捉えているかを表したグラフです。このグラフにある通り、鳥は人間にはない"第4のセンサー"を持っています。このセンサーは、紫外線の波長に反応するようになっているため、鳥には紫外線が見えている、と言われています。
                </p>

                <img src="/assets/products/feature/acalux/column/images/graph.jpg" width="430" height="470" alt="人間と鳥の色のとらえ方" class="photo02">

                <p class="honmon2">　紫外線は、鳥の目には一体どんな色に見えるのでしょうか？<br>
                その答えは、人間にはきっと永遠に分かりません。なぜなら、もしも私たちが紫外線を見ることができたとしたら、私たちが見たことのあるどの色とも違った色に見えるからです。赤とも青とも緑とも黄色とも違う、全く別の色に見えるはずです。しかし残念ながら、一度も見たことのない色を想像することはできません。<br>
                　"色"というのは人間が視覚で捉えることで初めて生じる概念です。<br>
                光の"色"というのは実は感覚指標であって、客観的な物理指標ではありません。そのため、色の見え方・感じ方には個人差や嗜好が強く出ます。錐体の波長による吸収度合いを表現したグラフも、色の感じ方を表しているわけではありません。<br>
                　自分が見ている"赤"は、隣の人が見ている"赤"と本当に同じように見えているだろうか、と考えたことはありませんか？その答えもきっと永遠に分からないことですが、考えれば考えるほど分からなくなってしまう、何だかとっても不思議な話ですね。
                </p>

