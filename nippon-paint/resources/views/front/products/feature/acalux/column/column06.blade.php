                <h2>"明るいと感じる部屋とはどんな部屋?"</h2>

                <p class="honmonfla">　照度はlux（ルクス）という指標で評価することが一般的です。<br>
                ところが照明の世界で、この指標では"人が感じる明るさ"をうまく評価できない場合が出てきました。特に近年になって増えてきた間接照明を利用した空間の場合などに、ルクスと人の感覚に大きな差が出るようになったのです。そのため近年、ルクスに替わる新たな明るさの指標として"Feu（フー）"という指標がつくられました。<br>
                　この指標は、パナソニック電工株式会社が近年開発したものです。<br>
                ルクスでは、照明から届く光を平面的に評価します（この面を作業面といいます）。ところが人間は、無意識のうちに視界に入る明るさ全体を感覚的に捉えています。そのため指向性の強い照明環境（上から一方向的に照らす）や、拡散性の強い照明環境（空間全体を一様に照らす）では、ルクスと感覚評価に大きなギャップが出ることが分かったのです。<br>
                　前者のような照明環境では、実際のルクス値は高くても、人間の感覚では暗く感じる傾向があります。逆に後者のような照明環境では、実際のルクス値が低くても、明るく感じる傾向があります。
                </p>

                <div id="flashContent">
                    <img src="/assets/products/feature/acalux/column/images/column06picto.jpg" width="430" height="240" alt="luxとFeuの指標">
                </div>

                <p class="honmon">　そこで、より人間の感覚に則した新たな指標として"Feu（フー）"が開発されたのです。Feuは平面的にではなく、3次元的に空間全体の明るさを評価する指標です。<br>
                　最近ではこのFeuを積極的に取り入れる動きが拡がってきました。Feuを測定するには、広角レンズを備えた特殊なデジタルカメラと、それを制御・計算するソフトウェアが必要になります。<br>
                　Feuでは、視界に入る空間全体を評価しますので、同じ平均照度（ルクス）の空間であっても壁面に従来内装塗料よりもアカルクスを使用した場合の方がFeuが高くなる傾向があります。空間の明るさに対する最新の考え方においても、アカルクスは有効です。
                </p>

