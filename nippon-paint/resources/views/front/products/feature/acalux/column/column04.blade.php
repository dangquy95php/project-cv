                <p class="honmon2">　光は、ガラスに入ると少し速度が遅くなります。そのため、速度が遅くなった方向へ曲げられて屈折するのです。光の速度は、密度が高い物質の中では少し遅くなる性質があります。空気よりも水やガラスの方が密度が高いため、光の速度が遅くなるのです。<br>
                　では、先ほどの陽炎現象は、なぜ屈折が起こるのでしょうか。<br>
                地上の熱であたためられた空気は熱で膨張します。そうすると、冷たい空気よりも少しだけ密度が薄くなります。そのため同じ空気の中なのに屈折現象がおこるというわけです。
