                <h2>"光はどうして曲がる?"</h2>

                <p class="honmonfla">　光は、水やガラスのような透明なものに入ると、進行方向が曲がる（屈折する）ことはよく知られています。水が入ったカップの底が、実際よりも浅く見えるのは屈折によるものです。眼鏡やレンズは、屈折の性質を利用したものです。<br>
                　実は陽炎も、屈折現象の一種です。<br>
                光の屈折は、光の速さが水やガラスの中では少し遅くなるために起こる現象です。これと同じことは、クルマの運転をする方であれば経験したことがある方もいらっしゃるかも知れません。雨の日、道路にできた大きな水溜りの上をクルマで走ったとき、ハンドルを取られた記憶がありませんか？<br>
                　左右の車輪の片側だけが水溜りに入ると、その車輪は速度が遅くなります。ところが反対側の車輪は速度がそのままのため、左右の車輪に速度の違いが生じます。そのため、クルマは速度が遅くなった車輪の方向に曲げられて、ハンドルを取られるのです。光の屈折は、この現象と同じです。
                </p>

