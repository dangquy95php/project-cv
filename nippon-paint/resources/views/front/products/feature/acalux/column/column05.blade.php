                <h2>"明るいままの部屋をつくることはできる?"</h2>

                <p class="honmonfla">　暗い部屋を明るい状態に保つには、照明で照らし続けなければなりません。照明を切ればとたんに暗くなってしまいます。<br>
                　照明を切っても明るい状態を保ち続けることはできないのでしょうか。<br>
                もしも壁・床・天井が鏡で出来た部屋をつくって、その中で一瞬だけフラッシュを発光させたら、光はずっと反射を繰り返し、部屋の中がいつまでも明るい状態に保たれないでしょうか。しかし結果は、そんな部屋でフラッシュを発光しても、すぐに光がなくなって暗くなってしまいます。とても反射率が高い鏡をつくっても、100％の反射率を持つ完全な鏡をつくることはできません。そのため、光が鏡に当たると少しだけ吸収されてしまいます。<br>
                光は非常に速いので、一瞬のあいだに無数の反射を繰り返します。なので1回の反射で吸収される光が少しだけであっても、一瞬のうちに光は全てなくなってしまうのです。<br>
                　光を"保存する"ことは、残念ながらできません。
                </p>

