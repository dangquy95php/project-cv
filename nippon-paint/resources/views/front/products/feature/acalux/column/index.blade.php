                <h2>"人間はどうやって光を見ている?"</h2>

                <p class="honmon">　人間の目には、光を捉えるセンサーの役割を果たす細胞があります。そのセンサーは"錐体"と呼ばれ、3つの種類があります。その3つのセンサーがそれぞれ違う色の光を捉えることで、人間は光を色として認識します。それぞれ赤・青・緑の光を捉える役割を果たしていて、この3色が"光の3原色"と言われています。<br>
                　そもそも、人間はこの3色の組み合わせで色を認識しているので、この3色を組み合わせれば、人間が見えるどんな景色も表現することができます。<br>
                　これを利用しているのがテレビです。<br>
                テレビモニターは、光の3原色の発光体の集合になっていて、それぞれの強弱を調整することで画像を映し出しています。
